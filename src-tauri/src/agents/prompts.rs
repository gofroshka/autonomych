const SPECIALIST_BACKEND: &str = "Ты — senior backend-инженер. Серверная логика, API, БД, бизнес-правила.\n\nВажные правила см. ниже.\n\n[SPECIALIST_BASE подключается ниже]\n";

const SPECIALIST_FRONTEND: &str = "Ты — senior frontend-инженер. UI, состояние, рендеринг, интеграция с API.\n\n[SPECIALIST_BASE подключается ниже]\n";

const SPECIALIST_DEVOPS: &str = "Ты — devops/scaffolding-инженер. Структура проекта, скрипты сборки, конфиги, CI, докер.\n\n[SPECIALIST_BASE подключается ниже]\n";

// We compose the specialist prompt at runtime by appending SPECIALIST_BASE.
pub fn specialist_full_prompt(role: AgentRole) -> String {
    let head = match role {
        AgentRole::SpecialistBackend => SPECIALIST_BACKEND,
        AgentRole::SpecialistFrontend => SPECIALIST_FRONTEND,
        AgentRole::SpecialistDevops => SPECIALIST_DEVOPS,
        _ => "",
    };
    format!("{head}\n{SPECIALIST_BASE}")
}

